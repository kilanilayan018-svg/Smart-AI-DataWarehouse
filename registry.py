# === INSERT FUNCTIONS (Add data) ===

def insert_run(dataset_id, status="started", phase=None):
    """Add a new pipeline run"""
    cursor.execute(
        "INSERT INTO runs (dataset_id, status, phase) VALUES (?, ?, ?)",
        (dataset_id, status, phase)
    )
    conn.commit()
    run_id = cursor.lastrowid
    print(f"✅ Run added: ID {run_id}")
    return run_id

def insert_dataset(dataset_id, filename, source, row_count, col_count, target, task_type, file_path):
    """Add dataset information"""
    cursor.execute("""
        INSERT INTO datasets
        (dataset_id, original_filename, source, row_count, column_count,
         target_column, task_type, file_path)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?)
    """, (dataset_id, filename, source, row_count, col_count, target, task_type, file_path))
    conn.commit()
    print(f"✅ Dataset added: {dataset_id}")

def insert_plan(dataset_id, plan_json, is_ai_generated=0):
    """Save a preprocessing plan"""
    import json
    cursor.execute("""
        INSERT INTO plans (dataset_id, plan_json, is_ai_generated)
        VALUES (?, ?, ?)
    """, (dataset_id, json.dumps(plan_json), is_ai_generated))
    conn.commit()
    plan_id = cursor.lastrowid
    print(f"✅ Plan added: ID {plan_id}")
    return plan_id

def insert_metrics(run_id, model_type, accuracy, f1_score):
    """Save model performance metrics"""
    cursor.execute("""
        INSERT INTO metrics (run_id, model_type, accuracy, f1_score)
        VALUES (?, ?, ?, ?)
    """, (run_id, model_type, accuracy, f1_score))
    conn.commit()
    print(f"✅ Metrics added for run {run_id}")

