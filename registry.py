# === FETCH FUNCTIONS (Read data) ===

def get_run(run_id):
    """Fetch a specific run by ID"""
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    cursor.execute("SELECT * FROM runs WHERE run_id = ?", (run_id,))
    result = cursor.fetchone()
    conn.close()
    return result

def get_runs_by_dataset(dataset_id):
    """Get all runs for a dataset"""
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    cursor.execute("SELECT * FROM runs WHERE dataset_id = ? ORDER BY start_time DESC", (dataset_id,))
    result = cursor.fetchall()
    conn.close()
    return result

def get_dataset(dataset_id):
    """Fetch dataset metadata"""
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    cursor.execute("SELECT * FROM datasets WHERE dataset_id = ?", (dataset_id,))
    result = cursor.fetchone()
    conn.close()
    return result

def get_all_datasets():
    """List all datasets"""
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    cursor.execute("SELECT dataset_id, original_filename, row_count, task_type FROM datasets")
    result = cursor.fetchall()
    conn.close()
    return result

def get_metrics_by_run(run_id):
    """Get metrics for a specific run"""
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    cursor.execute("SELECT * FROM metrics WHERE run_id = ?", (run_id,))
    result = cursor.fetchall()
    conn.close()
    return result

def get_recent_runs(limit=5):
    """Get the most recent runs (for UI dashboard)"""
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    cursor.execute("""
        SELECT r.run_id, r.dataset_id, r.start_time, r.status, r.phase,
               d.original_filename, m.accuracy, m.f1_score
        FROM runs r
        LEFT JOIN datasets d ON r.dataset_id = d.dataset_id
        LEFT JOIN metrics m ON r.run_id = m.run_id
        ORDER BY r.start_time DESC
        LIMIT ?
    """, (limit,))
    result = cursor.fetchall()
    conn.close()
    return result

# === UPDATE FUNCTIONS ===

def update_run_status(run_id, status, set_end_time=True):
    """Update run status and optionally set end time"""
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    if set_end_time:
        cursor.execute("""
            UPDATE runs
            SET status = ?, end_time = CURRENT_TIMESTAMP
            WHERE run_id = ?
        """, (status, run_id))
    else:
        cursor.execute("UPDATE runs SET status = ? WHERE run_id = ?", (status, run_id))
    conn.commit()
    conn.close()
    print(f"✅ Run {run_id} updated to {status}")

